package markup

// GetIndex returns the string template for the repo's index.html file.
func GetIndex() string {
	return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta http-equiv="refresh" content="0; url={{.url}}" />
  <title>{{.title}}</title>
</head>
<body>
  <p><a href="{{.url}}">Click here</a> if not redirected automatically.</p>
</body>
</html>`
}

// GetCname returns the string template for the repo's CNAME file.
func GetCname() string {
	return "{{.cname}}"
}

// GetReadme returns the string template for the repo's README.md file.
func GetReadme() string {
	return `### {{.title}}

Redirects to [{{.url}}]({{.url}}).

###### Automatically generated by [kshvmdn/shorten](https://github.com/kshvmdn/shorten).`
}
